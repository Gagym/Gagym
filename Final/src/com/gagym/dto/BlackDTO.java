package com.gagym.dto;

public class BlackDTO
{
	private String blackNo, memNo, blackDate, adminId;

	
	public String getBlackNo()
	{
		return blackNo;
	}

	public void setBlackNo(String blackNo)
	{
		this.blackNo = blackNo;
	}

	public String getMemNo()
	{
		return memNo;
	}

	public void setMemNo(String memNo)
	{
		this.memNo = memNo;
	}

	public String getBlackDate()
	{
		return blackDate;
	}

	public void setBlackDate(String blackDate)
	{
		this.blackDate = blackDate;
	}

	public String getAdminId()
	{
		return adminId;
	}

	public void setAdminId(String adminId)
	{
		this.adminId = adminId;
	}
	
}
