package com.gagym.dto;

public class ScheduleChangeDTO
{
	private String classNo, cerDate, startTimeNo, endTimeNo;

	public String getClassNo()
	{
		return classNo;
	}

	public void setClassNo(String classNo)
	{
		this.classNo = classNo;
	}

	public String getCerDate()
	{
		return cerDate;
	}

	public void setCerDate(String cerDate)
	{
		this.cerDate = cerDate;
	}

	public String getStartTimeNo()
	{
		return startTimeNo;
	}

	public void setStartTimeNo(String startTimeNo)
	{
		this.startTimeNo = startTimeNo;
	}

	public String getEndTimeNo()
	{
		return endTimeNo;
	}

	public void setEndTimeNo(String endTimeNo)
	{
		this.endTimeNo = endTimeNo;
	}
	
}
